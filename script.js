var rebuildSearchIndex = function() {
  search = new JsSearch.Search('id');
  search.tokenizer = new JsSearch.StemmingTokenizer(stemmer, search.tokenizer);
  search.tokenizer = new JsSearch.StopWordsTokenizer(search.tokenizer);
  search.indexStrategy = eval('new ' + 'JsSearch.PrefixIndexStrategy' + '()');
  search.sanitizer = eval('new ' + 'JsSearch.LowerCaseSanitizer' + '()');
  search.searchIndex = new JsSearch.TfIdfSearchIndex('id');
  search.addIndex('title');
  search.addIndex('Description/Web Link');
  search.addIndex('Asset Category');
  search.addDocuments(allAssets);
};
